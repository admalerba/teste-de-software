class Calculadora:
    
    def somar(self,num1, num2):
        return num1 + num2
    
    def subtrair(self,num1, num2):
        return num1 - num2
    
    def multiplicar(self,num1, num2):
        return num1 * num2  
    
    def dividir(self,num1, num2):
        return num1 / num2
    
    def potencia(self,num1, num2):
        return num1 ** num2 
    
    def raiz(self,num1):
        return num1 ** 0.5  
    
    def porcentagem(self,num1, num2):
        return (num1 * num2) / 100    